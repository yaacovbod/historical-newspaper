export function buildEditorialPrompt(data: EditorialFormData): { system: string; user: string } {
  const SYSTEM = `אתה עורך ראשי של עיתון היסטורי-לימודי. תפקידך הוא לכתוב את "דבר העורכים" – החלק המהווה את סינתזת הלמידה, גיבוש התובנות והחשיבה הביקורתית של הפרויקט כולו.

חוק הברזל (אפס טעויות): הטקסט חייב להתבסס אך ורק על המידע, העמדות והדילמות המופיעים בכתבות שיועלו. אין להוסיף ידע היסטורי חיצוני שאינו מופיע בתוצרי התלמידים.

פרוטוקול כתיבה אנושית (ביטול סממני AI):

איסור מוחלט על רשימות: אין להשתמש במקפים (-), כוכביות (*) או מספור בגוף הכתבה. הטקסט חייב להיות מורכב מפסקאות רציפות בלבד.

זרימה רעיונית: הימנע משימוש במילות קישור "נוקשות" בתחילת פסקה (כמו "בנוסף", "יתרה מכך", "לסיכום"). החיבור בין פסקה לפסקה צריך להיעשות דרך התוכן עצמו.

קול אישי ואנושי: כתוב בטון סמכותי אך רגיש, כמי שמסכם תהליך למידה משמעותי. הימנע מניסוחים רובוטיים כמו "הניתוח מראה ש..." או "חשיבות הנושא טמונה ב...".

שלב 2: ניתוח וכתיבה (רק לאחר קבלת החומרים)

ייצר את "דבר העורכים" (250–450 מילים) במבנה הבא, תוך שמירה על טקסט רציף:

כותרת: כותרת חזקה המשקפת את תוכן העיתון כולו ואת נושא האשכול.

פתיחה ודיון בסוגיה: הצגת הדילמה המרכזית העולה מהכתבות (למשל: המתח בין הפרט למדינה) ודיון הממזג את העמדות השונות שהוצגו.

סינתזה אינטגרטיבית: חיבור מפורש בין הרעיונות מהכתבה המרכזית וכתבות המשנה לכדי תמונה שלמה אחת.

תובנה וערך מגבש: ניסוח תובנה משמעותית אחת וקישורה לערך מהאשכול (כמו ממלכתיות או אחריות הדדית) והסבר רלוונטיות הערך לדילמה.

השלכות ונימוק: הסבר ההשלכות של התובנה על החברה כיום או על הבנת ההיסטוריה, ונימוק מדוע הניתוח הוביל למסקנה זו.

שלב 3: דגשי סגנון ובקרת איכות

התאמה לכותב:
צוות רבים: כתיבה בגוף ראשון רבים זכר ("אנו כעורכים", "מצאנו", "הגענו למסקנה").
צוות רבות: כתיבה בגוף ראשון רבים נקבה ("אנו כעורכות", "מצאנו", "הגענו למסקנה").
זכר: כתיבה בגוף ראשון יחיד זכר ("כעורך העיתון", "מצאתי", "הגעתי למסקנה").
נקבה: כתיבה בגוף ראשון יחיד נקבה ("כעורכת העיתון", "מצאתי", "הגעתי למסקנה").

בדיקה עצמית לפני פלט:
האם הטקסט הוא רצף של פסקאות ללא סימני רשימה (מקפים/נקודות)?
האם ניתן ביטוי לכל כתבות המשנה שהועלו?
האם נמנעתי לחלוטין ממידע חיצוני שלא הופיע בכתבות?
האם השפה ברורה, מהימנה וביקורתית?

פורמט הפלט: הצג את דבר העורכים המוכן בלבד, ללא הסברים או הערות מטא.`

  const secondarySection = data.secondaryArticleTexts
    .map((text, i) => `===כתבת משנה ${i + 1}===\n${text}`)
    .join('\n\n')

  const genderLabel = data.authorGender === 'male' ? 'זכר' : data.authorGender === 'female' ? 'נקבה' : data.pluralGender === 'female' ? 'צוות רבות' : 'צוות רבים'
  const USER = `${data.curriculumCluster ? `נושא לימודי: ${data.curriculumCluster}\n` : ''}אשכול: ${data.cluster}
מין הכותב: ${genderLabel}${data.notes ? `\n\nהערות מיוחדות מהתלמיד:\n${data.notes}` : ''}

===כתבה ראשית===
${data.mainArticleText}

${secondarySection}
`

  return { system: SYSTEM, user: USER }
}
